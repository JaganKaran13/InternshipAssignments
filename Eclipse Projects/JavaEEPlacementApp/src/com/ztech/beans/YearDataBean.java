package com.ztech.beans;

public class YearDataBean {

	private int year, studentCount, studentPlaced;
	
	public void setYear(int year) {
		this.year = year;
	}
	
	public void setStudentCount(int studentCount) {
		this.studentCount = studentCount;
	}
	
	public void setStudentPlaced(int studentPlaced) {
		this.studentPlaced = studentPlaced;
	}
	
	public int getYear() {
		return year;
	}
	
	public int getStudentCount() {
		return studentCount;
	}
	
	public int getStudentPlaced() {
		return studentPlaced;
	}
	
}
