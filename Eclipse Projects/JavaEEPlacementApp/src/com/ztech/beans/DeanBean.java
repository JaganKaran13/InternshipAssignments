package com.ztech.beans;

public class DeanBean {

	private String deptName;
	private int placedPercentage, studentCount, studentPlacedCount;

	public String getDeptName() {
		return deptName;
	}

	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}
	
	public int getPlacedPercentage() {
		return placedPercentage;
	}

	public void setPlacedPercentage(int placedPercentage) {
		this.placedPercentage = placedPercentage;
	}

	public int getStudentCount() {
		return studentCount;
	}

	public void setStudentCount(int studentCount) {
		this.studentCount = studentCount;
	}

	public int getStudentPlacedCount() {
		return studentPlacedCount;
	}

	public void setStudentPlacedCount(int studentPlacedCount) {
		this.studentPlacedCount = studentPlacedCount;
	}

}
